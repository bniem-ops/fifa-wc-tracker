/**
 * Given the 8 group letters whose third-place team qualified (any order),
 * returns { A, B, D, E, G, I, K, L } mapping each "3rd place" bracket slot
 * to the group letter whose third-place team fills it. Returns null if the
 * input isn't a valid set of exactly 8 distinct group letters.
 */
export function resolveThirdPlaceSlots(qualifyingGroupLetters) {
  if (!qualifyingGroupLetters || qualifyingGroupLetters.length !== 8) return null;
  const key = [...qualifyingGroupLetters].sort().join("");
  return ANNEX_C.get(key) || null;
}
